# Arithmatic Operator
a = 4
b = 2
print("==== Arithmatic Operators ====")
print('Addition a + b =',a+b)
print('Substraction a - b =',a-b)
print('Multiplication a * b =',a*b)
print('Division a / b =',a/b)
print('Exponent a ** b =',a**b)
print('Modulus a % b =',a%b)
print('Floor Division a // b =',a//b)
print(" ")
print("==== Bitwise Operators ====")
print('AND a & b =',a&b)
print('OR a | b =',a|b)
print('EXOR a ^ b =',a^b)
print('Not ~b =',~b)
print('Right shift a >> b =',a>>b)
print('Left Shift a << b =',a<<b)
print(" ")
print("==== Logical Operators ====")
print('AND a and b =',a and b)
print('OR a or b =',a or b)
print('Not not b =',not b)
print(" ")
print("==== Comparison Operators ====")
print('greater than a > b =',a>b)
print('lesser than a < b =',a<b)
print('Equal to a == b =',a==b)
print('greater than Eq to a >= b =',a>=b)
print('lesser than Eq to a <= b =',a<=b)
print('not eq to a != b =',a!=b)

